import math

pi = math.pi
angle_in_radians = 2 * pi

# converting angle in radians to degrees

angle_in_degrees = math.degrees(angle_in_radians)

print(angle_in_degrees)

# converting again to radians

radians = math.radians(angle_in_degrees)

print(radians)

